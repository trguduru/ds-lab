package org.tguduru.ds.lab.crackingcode.operators;

/**
 * Implementation of bit wise operators.
 */
public class Operators {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println(21 ^ -21); //xor
        System.out.println(21 & -21); // and
        System.out.println(21 | -21); // or

        System.out.println(4 >> 2);
        System.out.println(4 << 2);
    }
}
